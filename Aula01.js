let clima = prompt("Digite como está o clima:");
if (clima === "ensolarado") {
   alert("O sol brilha, sua energia também!");
} else if (clima === "chuvoso") {
   alert("A chuva cai, mas você sempre pode voar!");
} else if (clima === "nublado") {
    alert("O céu pode estar cinza, mas seu dia pode ser colorido!");
} else  {
    alert(" Não compreendi!");
}
